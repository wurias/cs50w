# LISTAS
# Defina uma lista de nomes.
names = ["Pedro", "José", "Antonio", "Franco"]

# método sort() classifica a lista em ordem crescente/alfabética.
names.sort()
print(names)

# Adcionando novos itens na lista.
names.append("Pereira")
print(names)

# Removendo itens da lista.
names.remove("José")
print(names)