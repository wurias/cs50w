# TUPLAS
# Uma coleção ordenada (ordem definida, e essa ordem não será alterada) 
# e imutável (não podemos alterar, adicionar ou remover itens após a criação da tupla)
# Permitem valores duplicados

point = (12.5, 23.6, 45, 12.8)

# Imprimir todos os valores da variável
print(point)

# Imprimir valor da variável indicado pelo índice
print(point[0]); print(point[1]); print(point[2]); print(point[3]);

# Uma tupla pode conter diferentes tipos de dados: 
